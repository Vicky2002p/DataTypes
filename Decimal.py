x = 7
print(f"The integer value of x is {x}")
x = float(x)
print(f"The float value of x is {x}")
y = 3.756
print(f"The value of y is {y}")
z = pow(x,y)
print(f"The value of z is {z}")
z = round(z)
print(f"The rounded value of z is {z}")
result = x/y
result = round(result, 3)
print(f"The result rounded to 3 decimal place is {result}")
